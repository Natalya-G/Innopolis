package org.example;

public class Flat {
    int numberRooms;
    int floor;
    double area;
    boolean balcony;

    public Flat(int numberRooms, int floor, double area, boolean balcony) {
        this.numberRooms = numberRooms;
        this.floor = floor;
        this.area = area;
        this.balcony = balcony;
    }
}
